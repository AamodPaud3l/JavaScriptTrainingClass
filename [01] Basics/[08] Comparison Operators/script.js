"use strict";

let var1 = 9;
let var2 = '9';

// Equal == (Compares only value)
console.log(var1 == var2);

// Equal === (compares value and type)
console.log(var1 === var2);

// Not Equal !=
console.log(var1 != var2);

// Not Equal !== (compares in equality for both value and types
console.log(var1 !== var2);

// Greater than >
console.log(var1 > var2);

// Less than <
console.log(var1 < var2);

// Greater than or equal to >=
console.log(var1 >= var2);

// Less than or equal to <=
console.log(var1 <= var2);