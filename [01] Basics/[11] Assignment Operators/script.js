"use strict";

// Assignment operator
let var1 = 9;
console.log(var1);

// Add Assignment operator
let var2 = 9;
var2 += 6;
console.log(var2);

// Subtract Assignment operator
let var3 = 9;
var3 -= 6;
console.log(var3);

// Multiplication Assignment operator
let var4 = 9;
var4 *= 6;
console.log(var4);

// Division Assignment operator
let var5 = 9;
var5 /= 6;
console.log(var5);

// Modulus Assignment operator
let var6 = 9;
var6 %= 6;
console.log(var6);






